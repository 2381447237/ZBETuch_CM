public class MainPagerGvInfo {

    private String title;

    private List<MainPagerLvInfo> lvInfoList;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public List<MainPagerLvInfo> getLvInfoList() {
        return lvInfoList;
    }

    public void setLvInfoList(List<MainPagerLvInfo> lvInfoList) {
        this.lvInfoList = lvInfoList;
    }

    public MainPagerGvInfo(List<MainPagerLvInfo> lvInfoList, String title) {
        this.lvInfoList = lvInfoList;
        this.title = title;
    }

    public static class MainPagerLvInfo {

        private String lvContent;
        private String lvTime;

        public MainPagerLvInfo(String lvContent, String lvTime) {
            this.lvContent = lvContent;
            this.lvTime = lvTime;
        }

        public String getLvContent() {
            return lvContent;
        }

        public void setLvContent(String lvContent) {
            this.lvContent = lvContent;
        }

        public String getLvTime() {
            return lvTime;
        }

        public void setLvTime(String lvTime) {
            this.lvTime = lvTime;
        }


    }

}
